<p>I'm also a plus size woman who's had to learn how to love myself and feel confident when my body doesn’t fit the cultural ideal. It's my goal to help women learn to love and respect their bodies, become as healthy as they can be, and live a plus size life!</p>

<p>If you've ever felt like you don't fit in with the cultural ideal, you are not alone! From a young age I compared myself to everyone around me and I was either obsessed with weight loss or avoiding even looking at myself.</p>

<p>My senior year of high school my parents and I decided to go on a diet together. We lost weight together and then we promptly gained it back. And that was the beginning of the “eat-repent-repeat” cycle for me.</p>

<p>In college and graduate school I also became interested in the idea of healthy body image and healthy eating versus eating disorders. I read everything I could get my hands on to help me understand my feelings. I was starting to learn the benefits of accepting my body, but I wasn’t ready to abandon the dream of being an “ideal weight”.</p>

<p>My first career was in public health. As you can imagine, there’s a big emphasis on nutrition, exercise, and weight in this field. I always felt that my weight was impeding my career and potential for advancement – even though I was competent at my job.</p>

